//js script for ph-veneer.html
// Get the modal
var iot1m = document.getElementById("iot-1-m");
// Get the image and insert it inside the modal - use its "alt" text as a caption
var iot1tr = document.getElementById("iot-1-tr");
var iot1mc = document.getElementById("iot-1-mc");
iot1tr.onclick = function(){
  iot1m.style.display = "block";
  iot1mc.src = this.src;
}
// Get the <span> element that closes the modal
var iot1c = document.getElementById("iot-1-c");
// When the user clicks on <span> (x), close the modal
iot1c.onclick = function() {
  iot1m.style.display = "none";
}
iot1m.onclick = function() {
  iot1m.style.display = "none";
}

