// Get the modal
var rv1m = document.getElementById("rv-1-m");
// Get the image and insert it inside the modal - use its "alt" text as a caption
var rv1tr = document.getElementById("rv-1-tr");
var rv1mc = document.getElementById("rv-1-mc");
rv1tr.onclick = function(){
  rv1m.style.display = "block";
  rv1mc.src = this.src;
}
// Get the <span> element that closes the modal
var rv1c = document.getElementById("rv-1-c");
// When the user clicks on <span> (x), close the modal
rv1c.onclick = function() {
  rv1m.style.display = "none";
}
rv1m.onclick = function() {
  rv1m.style.display = "none";
}

