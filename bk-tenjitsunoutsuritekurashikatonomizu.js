var tenjitsu12tr = document.getElementById("tenjitsu-12-tr");
var tenjitsu12mc = document.getElementById("tenjitsu-12-mc");
tenjitsu12tr.onclick = function(){
  tenjitsu12m.style.display = "block";
  tenjitsu12mc.src = this.src;
}
